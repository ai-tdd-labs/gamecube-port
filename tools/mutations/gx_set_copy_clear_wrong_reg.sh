#!/usr/bin/env bash
set -euo pipefail

# Mutation check for GXSetCopyClear: wrong BP reg id for first reg (0x50 instead of 0x4F)
#
# Usage:
#   tools/mutations/gx_set_copy_clear_wrong_reg.sh <test_case_dir> [<test_case_dir2> ...]

repo_root="$(cd "$(dirname "$0")/../.." && pwd)"
cd "$repo_root"

if [[ $# -lt 1 ]]; then
  echo "usage: $0 <test_case_dir> [<test_case_dir2> ...]" >&2
  exit 2
fi

args=("tools/run_mutation_check.sh" "tools/mutations/gx_set_copy_clear_wrong_reg.patch" "--")

first=1
for d in "$@"; do
  if [[ $first -eq 0 ]]; then
    args+=("::")
  fi
  first=0
  args+=("tools/run_host_scenario.sh" "$d")
done

"${args[@]}"
